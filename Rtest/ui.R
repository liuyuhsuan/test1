library(shiny)
library(leaflet)

# Define UI for application that draws a histogram
shinyUI(fluidPage(
  
  # Application title
  titlePanel("Old Faithful Geyser Data"),
  
  # Sidebar with a slider input for number of bins 
  sidebarLayout(
    sidebarPanel(
       sliderInput("bins",
                   "Number of bins:",
                   min = 1,
                   max = 50,
                   value = 30),
       numericInput("mean", 
                    label = h3("mean input"), 
                    value = 0),
       numericInput("var", 
                    label = h3("var input"), 
                    value = 1),
       numericInput("lng", 
                    label = h3("log input"), 
                    value = 121.537572),
       numericInput("lat", 
                    label = h3("lat input"), 
                    value = 25.015545),
       textInput("name",
                 label = h3("location name"),
                 value = 'NTU')
    ),
    
    # Show a plot of the generated distribution
    mainPanel(
       plotOutput("distPlot"),
       plotOutput("meanAndvar"),
       leafletOutput("map")
    )
  )
))
